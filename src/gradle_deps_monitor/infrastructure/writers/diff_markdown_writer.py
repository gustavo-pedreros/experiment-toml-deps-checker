"""DiffMarkdownWriter — serialises a FreezeDiff to Markdown."""

from __future__ import annotations

from pathlib import Path

from gradle_deps_monitor.domain.diff import (
    FindingChange,
    FreezeDiff,
    LibraryChange,
    PluginChange,
    VersionBump,
)
from gradle_deps_monitor.domain.finding import Severity
from gradle_deps_monitor.domain.severity_style import style_for
from gradle_deps_monitor.infrastructure.writers._atomic import atomic_write


def _severity_icon(severity_value: str) -> str:
    """Resolve a serialised severity string to its unified Markdown emoji.

    The diff loader stores severity as a plain string; mapping it back through
    :class:`Severity` keeps the diff writer aligned with the freeze writer
    (RFC-0016b).
    """
    try:
        return style_for(Severity(severity_value).to_common()).md_emoji
    except ValueError:
        return ""


_BUMP_LABEL = {
    VersionBump.MAJOR: "major",
    VersionBump.MINOR: "minor",
    VersionBump.PATCH: "patch",
    VersionBump.PRE_RELEASE: "pre-release",
}


class DiffMarkdownWriter:
    """Writes a :class:`~gradle_deps_monitor.domain.diff.FreezeDiff` as Markdown."""

    def write(self, diff: FreezeDiff, dest: Path) -> None:
        """Write *diff* to *dest*, creating parent directories as needed."""
        with atomic_write(dest) as fh:
            fh.write(_render(diff))


# ---------------------------------------------------------------------------
# Rendering
# ---------------------------------------------------------------------------


def _render(diff: FreezeDiff) -> str:
    lines: list[str] = []

    if diff.is_baseline:
        _render_baseline(diff, lines)
    else:
        _render_diff(diff, lines)

    return "\n".join(lines) + "\n"


def _render_baseline(diff: FreezeDiff, lines: list[str]) -> None:
    ts = diff.after_generated_at.strftime("%Y-%m-%d")
    lines += [
        "# 🌱 Baseline Established",
        "",
        f"**Generated:** {diff.after_generated_at.isoformat(timespec='seconds')}",
        "",
        "This is the first registered freeze report."
        " Future diff reports will compare against this baseline.",
        "",
        "> **Recommendation:** review non-stable versions and catalog health"
        " findings as a starting point",
        "> to prioritize technical debt for the next cycle.",
        "",
        "---",
        f"*Baseline established on {ts}.*",
    ]


def _render_diff(diff: FreezeDiff, lines: list[str]) -> None:
    before_date = diff.before_generated_at.strftime("%Y-%m-%d") if diff.before_generated_at else "?"
    after_date = diff.after_generated_at.strftime("%Y-%m-%d")

    lines += [
        f"# Freeze Diff: {before_date} → {after_date}",
        "",
        "| | Before | After |",
        "|---|---|---|",
        f"| Timestamp | {diff.before_generated_at.isoformat(timespec='seconds') if diff.before_generated_at else '—'}"  # noqa: E501
        f" | {diff.after_generated_at.isoformat(timespec='seconds')} |",
        "",
    ]

    # --- Libraries ---
    lines.append("## Libraries")
    lines.append("")
    _render_lib_summary(diff, lines)

    if diff.libraries_major:
        lines += ["", "### Upgraded — Major", ""]
        _lib_table(diff.libraries_major, lines)

    if diff.libraries_minor:
        lines += ["", "### Upgraded — Minor", ""]
        _lib_table(diff.libraries_minor, lines)

    if diff.libraries_patch:
        lines += ["", "### Upgraded — Patch", ""]
        _lib_table(diff.libraries_patch, lines)

    pre = [c for c in diff.library_changes if c.bump is VersionBump.PRE_RELEASE]
    if pre:
        lines += ["", "### Upgraded — Pre-release", ""]
        _lib_table(pre, lines)

    if diff.libraries_added:
        lines += ["", "### Added", ""]
        _lib_added_table(diff.libraries_added, lines)

    if diff.libraries_removed:
        lines += ["", "### Removed", ""]
        _lib_removed_table(diff.libraries_removed, lines)

    if diff.libraries_downgraded:
        lines += ["", "### Downgraded ⚠️", ""]
        _lib_table(diff.libraries_downgraded, lines)

    if not diff.library_changes:
        lines += ["", "_No library changes._"]

    # --- Plugins ---
    lines += ["", "## Plugins", ""]
    _render_plugin_summary(diff, lines)

    if diff.plugin_changes:
        lines += ["", "### Changes", ""]
        _plugin_table(diff.plugin_changes, lines)
    else:
        lines += ["", "_No plugin changes._"]

    # --- Health findings ---
    lines += ["", "## Catalog Health", ""]

    if diff.finding_changes:
        _findings_table(diff.finding_changes, lines)
    else:
        lines += ["_No changes in catalog health findings._"]

    lines += ["", "---", "*Generated by gradle-deps-monitor.*"]


def _render_lib_summary(diff: FreezeDiff, lines: list[str]) -> None:
    upgraded = len(diff.libraries_upgraded)
    added = len(diff.libraries_added)
    removed = len(diff.libraries_removed)
    downgraded = len(diff.libraries_downgraded)
    major = len(diff.libraries_major)
    minor = len(diff.libraries_minor)
    patch = len(diff.libraries_patch)

    lines.append("| Metric | Count |")
    lines.append("|--------|-------|")
    lines.append(f"| Upgraded | {upgraded} ({major} major, {minor} minor, {patch} patch) |")
    lines.append(f"| Added | {added} |")
    lines.append(f"| Removed | {removed} |")
    lines.append(f"| Downgraded | {downgraded} |")


def _render_plugin_summary(diff: FreezeDiff, lines: list[str]) -> None:
    upgraded = len(diff.plugins_upgraded)
    added = len(diff.plugins_added)
    removed = len(diff.plugins_removed)
    downgraded = len(diff.plugins_downgraded)

    lines.append("| Metric | Count |")
    lines.append("|--------|-------|")
    lines.append(f"| Upgraded | {upgraded} |")
    lines.append(f"| Added | {added} |")
    lines.append(f"| Removed | {removed} |")
    lines.append(f"| Downgraded | {downgraded} |")


def _lib_table(changes: tuple[LibraryChange, ...] | list[LibraryChange], lines: list[str]) -> None:
    lines.append("| Alias | Coordinate | Before | After |")
    lines.append("|-------|-----------|--------|-------|")
    for c in sorted(changes, key=lambda x: x.alias):
        before = c.before_version or "—"
        after = c.after_version or "—"
        lines.append(f"| `{c.alias}` | `{c.coordinate}` | {before} | {after} |")


def _lib_added_table(changes: tuple[LibraryChange, ...], lines: list[str]) -> None:
    lines.append("| Alias | Coordinate | Version |")
    lines.append("|-------|-----------|---------|")
    for c in sorted(changes, key=lambda x: x.alias):
        lines.append(f"| `{c.alias}` | `{c.coordinate}` | {c.after_version} |")


def _lib_removed_table(changes: tuple[LibraryChange, ...], lines: list[str]) -> None:
    lines.append("| Alias | Coordinate | Version |")
    lines.append("|-------|-----------|---------|")
    for c in sorted(changes, key=lambda x: x.alias):
        lines.append(f"| `{c.alias}` | `{c.coordinate}` | {c.before_version} |")


def _plugin_table(changes: tuple[PluginChange, ...], lines: list[str]) -> None:
    lines.append("| Alias | Plugin ID | Before | After | Kind |")
    lines.append("|-------|-----------|--------|-------|------|")
    for c in sorted(changes, key=lambda x: x.alias):
        before = c.before_version or "—"
        after = c.after_version or "—"
        lines.append(f"| `{c.alias}` | `{c.plugin_id}` | {before} | {after} | {c.kind} |")


def _findings_table(changes: tuple[FindingChange, ...], lines: list[str]) -> None:
    lines.append("| Status | Severity | Rule | Message |")
    lines.append("|--------|----------|------|---------|")
    for f in sorted(changes, key=lambda x: (x.status, x.rule_id)):
        icon = "🆕" if f.status == "introduced" else "✅"
        sev_icon = _severity_icon(f.severity)
        lines.append(
            f"| {icon} {f.status.capitalize()}"
            f" | {sev_icon} {f.severity}"
            f" | `{f.rule_id}`"
            f" | {f.message} |"
        )
